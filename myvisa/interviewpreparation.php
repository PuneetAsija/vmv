                            <h4>There are two basic question formats: directive and open-ended.</h4>
                            <p>Directive questions typically verify or clarify information. Most of these questions can be answered in 1-2 sentences. Open-ended questions are used to expand previous answers, and take more time.</p>

                            <h4>Students Related Interview Preparation:</h4>
                            <p>Interviews conducted by various Institutes abroad are very rare and are usually limited to some business schools. You most probably will not have to go through one of them. But, for your information, Wharton visits Delhi, Bombay and some other cities every year to interviews. Some schools conduct interviews telephonically and some others through their alumni who are based in the city you live in. If you happen to be applying to an institute that wants to interview, you must to be prepared.</p>

