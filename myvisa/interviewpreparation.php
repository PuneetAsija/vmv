                            <p>3) Name some other schools that you have applied to?</p>
                            <p>4) Tell me about yourself.</p>
                            <p>5) What are your career goals?</p>
                            <p>6) Where do you see yourself in ten years?</p>
                            <p>7) What value can you add to the program?</p>
                            <p>8) What are your greatest achievements?</p>
                            <p>9) What do you consider your three top strengths/weaknesses?</p>
