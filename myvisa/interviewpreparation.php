                            <p>a) <strong> Undergraduate Education :</strong> Remember your interviewers are from the academia, so give adequate importance to learning and education. Show your commitment towards learning and growing. Talk about your extracurricular activities and how these activities have helped you grow holistically. Focus and put extra emphasis on your contribution as well as what you have learnt. Describe how your schooling had helped you in achieving your career goals. Describe any leadership experiences and what you learnt from those experiences.</p>
                            <p>b) <strong>Work Experience :</strong> Be enthusiastic about your job experience. Portray yourself as one who tries to exceed expectations. Collective teamwork, inspiration, continuous learning and ownership. Be positive when talking about your boss or your firm. If you have changed jobs, it must show your motivation which leads to your desire for more challenges, more responsibilities, and the opportunity to grow and so on. Avoid negative comments like unappreciated, underpaid etc. Don’t be afraid to talk about a failure. Stress on what you learned from your failure. Describe your leadership experiences at work. Leadership potential is a very important quality desired by all MBA programs.</p>
                            <p>c) <strong>Goals :</strong> Show that you are committed to your career objectives. Your goals should be consistent with your experience and your desire to pursue further education. Be practical where you see yourself in five years ( refrain from making a statement like "I will be the CEO of Hewlett-Packard") and be prepared to demonstrate how advanced training and education will help you achieve your goals. Highlight some of the strengths of the program to further justify your choice. Make sure that all your answers are connected and reflect your thought for planning your future.</p>
                            <p>d) <strong>Personal :</strong> Be sure to prepare a brief outline of your dream. Take every opportunity to show that you are achievement oriented and strive to develop both personally and professionally. At the same time, show yourself to be a well-balanced and sensible person with varied interests. In your choice of books, it does not matter whether it is science fiction or biographies: the idea is to show yourself as knowledgeable in whatever interests you pursue.</p>
                          
